            <h1 class="title">INGRESOS TRIBUTARIOS DE MARZO ALCANZARON LOS S/ 11 721 MILLONES</h1>
            <p>
            Los ingresos tributarios del Gobierno Central Netos descontando las devoluciones de impuestos alcanzaron los S/ 11 721 millones durante marzo, manteniendo la tendencia de los últimos meses.
          </p>
            <p>
              Este crecimiento en la recaudación se encuentra sustentado en los siguientes factores principales:
            </p>
            <ul>
              <li>Los menores pagos a nivel de los tributos aduaneros, motivados por las menores importaciones y el menor tipo de cambio promedio. A lo anterior se une el efecto del mayor pago asociado a las importaciones garantizadas -principalmente combustibles- realizado durante el último día hábil de marzo y que se acreditará en las cuentas del Tesoro Público como recaudación del mes de abril.</li>
              <li>En contraposición, el desempeño de la actividad económica de febrero -mes vinculado a la mayoría de las obligaciones tributarias internas por las que se pagan tributos durante el mes de marzo- habría sido positivo, aunque con tasas de crecimiento menores, compensando parcialmente el efecto negativo de los factores mencionados..</li>
            </ul>
            <p>
            Resultados por tributos
          </p>
          <ul>
              <li>Impuesto a la Renta: En marzo, se recaudaron S/ 6 182 millones por este concepto. Se registraron reducciones en los pagos por Regularización, Segunda Categoría,  los pagos a cuenta del Régimen General y Régimen Mype Tributario - RMT, Primera Categoría, Régimen Especial de Renta, Cuarta Categoría, Quinta Categoría y en el rubro correspondiente al Resto de Rentas.</li>
              <li>Impuesto General a las Ventas (IGV): La recaudación del IGV alcanzó los S/ 6 200 millones en marzo.</li>
            </ul>
          <p>
            El IGV Interno recaudó S/ 3 819 millones, resultado asociado a los menores pagos realizados por las empresas vinculadas a la producción y comercialización de gas. Por su parte, el IGV que grava a las importaciones recaudó S/ 2 381 millones, influenciado por las menores importaciones, el menor tipo de cambio y los menores pagos correspondientes a las importaciones garantizadas. 
          </p>
          <ul>
            <li>Impuesto Selectivo al Consumo (ISC): La recaudación del ISC alcanzó S/ 670 millones en marzo.</li>
          </ul>
          <p>
          El ISC Interno se incrementó en 8,1%, debido a la evolución de las ventas de los productos afectos a este impuesto, principalmente cervezas, al desempeño de la demanda interna y al menor nivel de crédito aplicado por una empresa de hidrocarburos.</p>
          <ul>
            <li>
            Otros ingresos: La recaudación de marzo asociada a este rubro ascendió a S/ 493 millones. Dicho resultado se debió principalmente a los menores pagos por Multas, los Ingresos Como Recaudación, Impuesto Especial a la Minería, Impuesto a los Casinos y Tragamonedas, Fraccionamientos, Impuesto Temporal a los Activos Netos - ITAN y el Régimen Único Simplificado. Por el contrario, se registraron mayores pagos en Impuesto a las Transacciones Financieras - ITF.
            </li>
          </ul>

