            <h1 class="title">INGRESOS TRIBUTARIO DE FEBRERO ALCANZARON LOS s/ 11 366 MILLONES</h1>
            <p>
              Los ingresos tributarios del Gobierno Central Netos (descontando las devoluciones de impuestos) alcanzaron los S/ 11 366 millones durante el mes de febrero de 2024.         
           </p>
            <p>
              El desempeño de la recaudación de febrero se sustenta principalmente en los siguientes factores:            
            </p>
            <ul>
              <li>El desempeño de la actividad económica de enero -mes vinculado a la mayoría de las obligaciones tributarias internas por las que se pagan tributos durante febrero- fue levemente positivo, lo que habría compensado parcialmente los resultados..</li>
              <li>El efecto estadístico en contra generado tanto a nivel de las devoluciones como de los pagos por parte de los contribuyentes considerados MYPES.</li>
            </ul>
            <p>
             Es oportuno mencionar que en febrero del año 2023 se registró una reducción de las devoluciones. De otra parte, en el mismo mes ocurrió el pago de las obligaciones tributarias de las MYPES que vencieron en enero del 2023 y fueron postergadas. 
              Ambos eventos incrementan la base de comparación contra la recaudación de febrero del año 2024.          
            </p>
          <ul>
              <li>Los menores pagos de los tributos aduaneros, por la reducción de las importaciones garantizadas -principalmente combustibles- y las importaciones corrientes de febrero del 2024.</li>
              <li>El impacto negativo generado por cambios normativos vigentes en febrero del año 2024 pero no en febrero del 2023. </li>
            </ul>
          <p>
          Al respecto, destaca la aplicación de la Ley N° 31903 que dispone la libre disposición de fondos de las cuentas de detracciones pertenecientes a las empresas consideradas MYPES. A lo anterior se añade el efecto de la Ley N° 31962, que modificó la aplicación de los intereses tanto en las devoluciones como en la actualización de las multas.          
          </p>
          <ul>
              <li>Resultados por tributos</li>
              <li>Impuesto a la Renta: En febrero, se recaudaron S/ 5 116 millones por este concepto, importe que representa una disminución de 4,2% en comparación con el mismo mes del año 2023.</li>
            </ul>
            <p>
            Se observaron incrementos en los pagos correspondientes al Impuesto a la Renta de No Domiciliados (42,6%), Quinta Categoría (5.6%), Primera Categoría (6,6%) y Régimen Especial de Renta (9,6%). Por el contrario, se registraron contracciones en los pagos por Regularización, los pagos a cuenta del Régimen General y Régimen Mype Tributario - RMT, Cuarta Categoría, Segunda Categoría y en el rubro correspondiente al Resto de Rentas.           
          </p>
          <ul>
              <li>Impuesto General a las Ventas (IGV): La recaudación del IGV alcanzó los S/ 6 426 millones en febrero, lo que representa una disminución de 5,3% en comparación con el mismo mes del año 2023.</li>
            </ul>
